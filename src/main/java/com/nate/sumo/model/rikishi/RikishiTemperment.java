package com.nate.sumo.model.rikishi;

public class RikishiTemperment {

	private Integer focus;
	private Integer anger;
	private Integer drive;
	private Integer iq;
	
	public RikishiTemperment(){}

	public Integer getFocus() {
		return focus;
	}

	public void setFocus(Integer focus) {
		this.focus = focus;
	}

	public Integer getAnger() {
		return anger;
	}

	public void setAnger(Integer anger) {
		this.anger = anger;
	}

	public Integer getDrive() {
		return drive;
	}

	public void setDrive(Integer drive) {
		this.drive = drive;
	}

	public Integer getIq() {
		return iq;
	}

	public void setIq(Integer iq) {
		this.iq = iq;
	}
	
	
	
}
